% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
 I = handles.I;
    t = statxture(I,0.1);
    grayLevel = t(1);
    averageContarst = t(2);
    measureSmoothnes = t(3);
    thirdMoment = t(4);
    measureUni = t(5);
    Entropy = t(6);
    R = I(:,:,1);
    G = I(:,:,2);
    B = I(:,:,3);
    sumR = sum(sum(R));
    sumG = sum(sum(G));
    sumB = sum(sum(B));

    input = [grayLevel;averageContarst;measureSmoothnes;thirdMoment;measureUni;Entropy;sumR;sumG;sumB];
    
    
load net

output = abs(sim(net,input));


if output(1) > output(3) && output(1) > output(2)
    kelas = 'Apple Envy';
    if output(1) > 1 
        output(1) = 1;
    end
    if output(1) < 0
        output(1) = 0;
    end
    error = 1 - output(1);
elseif output(2) > output(3)&& output(2) > output(1)
    kelas = 'Apple Fuji';
    if output(2) > 1 
        output(2) = 1;
    end
    if output(2) < 0
        output(2) = 0;
    end
    error = 1 - output(2);
elseif output(3) > output(2) && output(3) > output(1)
    kelas = 'Apple Golden';
    if output(3) > 1 
        output(3) = 1;
    end
    if output(3) < 0
        output(3) = 0;
    end
    error = 1 - output(3);
else
    kelas = 'Unknown';
end

akurasi = (1-error) * 100;

set(handles.edit11,'String',kelas)
set(handles.edit1,'String',grayLevel)
set(handles.edit2,'String',averageContarst)
set(handles.edit3,'String',measureSmoothnes)
set(handles.edit4,'String',thirdMoment)
set(handles.edit5,'String',measureUni)
set(handles.edit6,'String',Entropy)
set(handles.edit7,'String',sumR)
set(handles.edit8,'String',sumG)
set(handles.edit9,'String',sumB)
set(handles.output1,'String',output(1))
set(handles.output2,'String',output(2))
set(handles.output3,'String',output(3))
set(handles.akurasi,'String',akurasi)




